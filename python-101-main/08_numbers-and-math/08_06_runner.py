# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)

# convert 10 miles to kilometers
distance = 1.6 * 10

# convert 30minutes 30 seconds to hours
time = (30 / 60) + (30 /3600)

# speed = distance / time
speed = distance / time

print (speed)